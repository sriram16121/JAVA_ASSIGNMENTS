import java.util.*;

public class sort_array {
    public static void main(String[] args) {
        String[] sa = { "abc", "bca", "afhsdh" };
        Arrays.sort(sa);
        System.out.println(Arrays.toString(sa));
        int[] arr = { 1, 2, 3, 4, 8, 7 };
        Arrays.sort(arr);
        System.out.println(Arrays.toString(arr));
    }
}
